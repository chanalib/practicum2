import { useNavigate } from "react-router-dom";
import { MusicHeader } from "../music-header";

const Home = () => {
    const navigate = useNavigate();

    function handleLogout(): void {
        // Clear user session or authentication data
        localStorage.removeItem("userToken");
        localStorage.removeItem("userData");

        // Redirect to the login page
        navigate('/login');
    }

    return (
        
        <div className="in">
        <div className="container">
            <div className="bigTitle">
            <MusicHeader onLogout={handleLogout} />

            <h1 className="home-title">קסם של צלילים</h1>
            <h2 className="home-title">אלפי שירים מחכים לכם, מה תרצו לשמוע?</h2>
             
            </div>
        </div>
        </div>
    );
};

export default Home;
